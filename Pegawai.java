public class Pegawai {
	private String namaPegawai;
	private int laundryMasuk;
	private int beratLaundry;



	public String getNamaPegawai() {
		return namaPegawai;
	}

	public void setNamaPegawai(String namaPegawai) {
		this.namaPegawai = namaPegawai;
	}

	public int getLaundryMasuk() {
		return laundryMasuk;
	}

	public void setLaundryMasuk(int laundryMasuk) {
		this.laundryMasuk = laundryMasuk;
	}

	public int getBeratLaundry() {
		return beratLaundry;
	}

	public void setBeratLaundry(int beratLaundry) {
		this.beratLaundry = beratLaundry;
	}

}
